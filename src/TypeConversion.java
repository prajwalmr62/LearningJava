
public class TypeConversion {
	
	public static void main(String[] args){
		float p2,p3,p4;
		int Check;
		p2= (float) 25.01;
		Check = (int)p2;
		System.out.println("Check"+Check);
		p3=(float) 23.02;
		Check = (int)p3;
		System.out.println("Check"+Check);
		p4=(float)22.10;
		Check=(int)p4;
		System.out.println("Check"+Check);
		return;
		
	}

}
